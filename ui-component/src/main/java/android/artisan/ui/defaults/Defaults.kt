package android.artisan.ui.defaults

/**
 * The aspect ratio (width/height) of product images.
 *
 * This constant defines the desired aspect ratio for product images displayed within the application.
 * It's used to maintain consistency in the visual presentation of product images,
 * ensuring they are displayed with a specific proportion.
 *
 * For instance, an aspect ratio of 0.75 implies that the width of the image should be 75% of its height,
 * i.e., for every 100 pixels of height, the width will be 75 pixels.
 * This can be considered as a portrait orientation.
 *
 *  Value: 0.75f
 */
public const val PRODUCT_IMAGE_ASPECT_RATIO: Float = 0.75f
